"""Tests for Verilog generator V4 (with smart connection engine)."""

import pytest

from topstitcher.core.data_model import (
    PortInfo, PortDirection, ModuleInfo, InstanceInfo,
    PortAssignment, ParamInfo,
)
from topstitcher.core.connection_engine import ConnectionEngine
from topstitcher.core.verilog_generator import VerilogGenerator


@pytest.fixture
def generator():
    return VerilogGenerator()


def build_test_design(module_name="top_module"):
    adder_mod = ModuleInfo(
        name="adder",
        ports=[
            PortInfo("clk", PortDirection.INPUT, 1, "0", "0"),
            PortInfo("rst_n", PortDirection.INPUT, 1, "0", "0"),
            PortInfo("a", PortDirection.INPUT, 8, "7", "0"),
            PortInfo("b", PortDirection.INPUT, 8, "7", "0"),
            PortInfo("sum", PortDirection.OUTPUT, 8, "7", "0"),
        ],
    )
    reg_mod = ModuleInfo(
        name="register",
        ports=[
            PortInfo("clk", PortDirection.INPUT, 1, "0", "0"),
            PortInfo("rst_n", PortDirection.INPUT, 1, "0", "0"),
            PortInfo("d", PortDirection.INPUT, 8, "7", "0"),
            PortInfo("q", PortDirection.OUTPUT, 8, "7", "0"),
        ],
    )
    adder = InstanceInfo.from_module(adder_mod, "u_adder")
    register = InstanceInfo.from_module(reg_mod, "u_register")
    engine = ConnectionEngine()
    global_sigs = ["clk", "rst_n"]
    assignments = engine.build_assignments(
        [adder, register], global_signals=global_sigs
    )
    return engine.resolve_design(
        module_name, [adder, register], assignments,
        global_signals=global_sigs,
    )


class TestOutputFormat:
    def test_has_timescale(self, generator):
        code = generator.generate(build_test_design())
        assert "`timescale 1ns / 1ps" in code

    def test_has_module_declaration(self, generator):
        code = generator.generate(build_test_design())
        assert "module top_module (" in code
        assert "endmodule" in code

    def test_has_header_comment(self, generator):
        code = generator.generate(build_test_design())
        assert "Auto-generated by TopStitcher" in code

    def test_custom_module_name(self, generator):
        code = generator.generate(build_test_design("my_chip"))
        assert "module my_chip (" in code


class TestPortDeclarations:
    def test_input_ports(self, generator):
        code = generator.generate(build_test_design())
        assert "input" in code
        # a, b are unique inputs → top-level
        assert "a" in code
        assert "b" in code

    def test_output_ports(self, generator):
        code = generator.generate(build_test_design())
        assert "output" in code
        # q is unique output → top-level
        assert "q" in code

    def test_global_signals_are_top_ports(self, generator):
        code = generator.generate(build_test_design())
        lines = code.split("\n")
        port_section = []
        in_port = False
        for line in lines:
            if "module " in line and "(" in line:
                in_port = True
            if in_port:
                port_section.append(line)
            if in_port and ");" in line:
                break
        port_text = "\n".join(port_section)
        assert "clk" in port_text
        assert "rst_n" in port_text


class TestSuggestedWire:
    def test_suggested_connection_creates_wire(self, generator):
        """sum→d suggestion creates internal wire sum_to_d."""
        design = build_test_design()
        code = generator.generate(design)
        # sum_to_d should be an internal wire
        assert "sum_to_d" in code
        assert "wire" in code


